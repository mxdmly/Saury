SELECT 
TDIAGNOSE.FID,
TDIAGNOSE.FPRN,
TDIAGNOSE.FTIMES,
TDIAGNOSE.FPX,
CASE 
	WHEN TDIAGNOSE.FZDLX = 's' THEN 'S' 
	ELSE TDIAGNOSE.FZDLX 
END,
TDIAGNOSE.FJBNAME,
TDIAGNOSE.FICDM,
CASE 
	WHEN TDIAGNOSE.FRYBQBH = '' OR TDIAGNOSE.FRYBQBH IS NULL THEN '-' 
	ELSE TDIAGNOSE.FRYBQBH 
END,
CASE 
	WHEN TDIAGNOSE.FRYBQ = '' OR TDIAGNOSE.FRYBQ IS NULL THEN '-' 
	ELSE TDIAGNOSE.FRYBQ 
END 

FROM 
bagl_java.dbo.TDIAGNOSE 
WHERE 
TDIAGNOSE.FID 